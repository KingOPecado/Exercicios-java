package comissao1908;


public class Funcionario {
    String nomes;
    double salarioBase;
    double vendas;
    char categoria;

    public Funcionario(String nomes, double salarioBase, double vendas, char categoria) {
        this.nomes = nomes;
        this.salarioBase = salarioBase;
        this.vendas = vendas;
        this.categoria = categoria;
    }
}
